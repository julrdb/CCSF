public class Ex3_15_do_whileLoop
{
	public static void main(String [] args)
	{
		
		int i=1;
		
		do
		{
			
			System.out.println(i);
			i+=2;
			
		}
		while(i<=10);
		
		

		System.out.println("I am outside the loop");
		System.out.println(i);
		
	}
}
		
